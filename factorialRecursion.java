public class Main {
    static int factorialRecursion(int n) {
        if (n == 0 || n == 1) {   // Base case
            return 1;
        }
        return n * factorial(n - 1); // Recursive call
    }

    public static void main(String[] args) {
        System.out.println(factorial(5)); // 120
    }
}
